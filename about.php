    <h1>Over deze website</h1>
    <p>
      Zoveel speeltuinen in Nederland, maar ze zijn online lastig te vinden... Zelf had ik al wel een lijstje met vrienden opgezet, maar landelijk bleef het lastig. Als vader breng ik een hoop tijd door in speeltuinen. Het liefst bezoek ik dan verschillende speeltuinen.
      <br>
      Zo ontstond het idee voor deze website: een overzicht van leuke speeltuinen met een filter om zo snel een geschikte speeltuin te vinden. Het doel? Vaker met de kinderen naar een speeltuin, nieuwe verschillende speeltuinen ontdekken, onontdekte speeltuinen bezoeken. Gelukkig heeft elk dorp en elke wijk wel een speeltuin, waardoor afwisselen goed te doen is.
      <hr>
      <h2>Wie zijn wij?</h2>
      Ik ben Pieter Palsma, vader van twee kinderen. Graag buiten en op pad naar nieuwe dingen. Soms speeltuinen, soms geocaches, soms fietstochten.
      Samen met Rijk van Putten hebben we dit idee vervolgens omgezet in deze website. Rijk zocht een opdracht voor het vak "Informatica" op het CSG Augustinus en ik ben daar docent. Al snel bleek dat de opdracht goed genoeg was om uit te breiden naar een echte website, en zie hier het resultaat.
